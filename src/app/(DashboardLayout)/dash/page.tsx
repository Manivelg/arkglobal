import React from "react";
import getContactDatas from "./_actions";
import Starter from "./_components/Starter";

const Page = async () => {
  const response = await getContactDatas();
  if (!response) {
    return <h1>Data not found</h1>;
  }

  return (
    <>
      <Starter data={response.data} />
    </>
  );
};

export default Page;
